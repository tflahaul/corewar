#include "../headers/libftprintf.h"

void					ft_lowerstr(char **str)
{
	t_uint		i;

	i = 0;
	while ((*str)[i])
		if (ft_iscapital((*str)[i++]))
			(*str)[i - 1] |= 32;
}
