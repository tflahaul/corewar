#include "../headers/libftprintf.h"

char			*ft_strcpy(char *s1, char const *s2)
{
	char		*ptr;

	ptr = s1;
	while (*s2)
		*s1++ = *s2++;
	*s1 = 0;
	return (ptr);
}
