#include "../headers/libftprintf.h"

char					*ft_strncpy(char *s1, char const *s2, size_t n)
{
	char				*ptr;

	ptr = s1;
	while (*s2 && n > 0)
	{
		*s1++ = *s2++;
		n--;
	}
	while (n > 0)
	{
		*s1++ = 0;
		n--;
	}
	return (ptr);
}
